package SimpleOddEven;

public class Print_OddEven_1To100 {

	public static void main(String[] args) {
		
		
		for (int i = 0; i <=100; i++) 
		{
			if(i%2==0) 
			{
				System.out.println(i+ " No is Even");	
			}
			
			else
			{
				System.out.println(i+ " No is Odd");
			}
	
		}

	}
}